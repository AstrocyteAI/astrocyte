# ───────────────────────────────────────────────────────────────────────────
# Markdown synthesis
# ───────────────────────────────────────────────────────────────────────────

def conversation_to_markdown(conv: dict, sample_id: str) -> str:
    """Render a LoCoMo conversation as PageIndex-friendly markdown.

    Sessions become ``## Session N (timestamp)`` headers — exactly what
    ``extract_nodes_from_markdown`` in PageIndex picks up as tree nodes.
    Each turn carries its original ``dia_id`` so the agent can cite
    sources (``D1:3``) the way the LoCoMo dataset annotates ground-truth
    evidence.
    """
    speaker_a = conv.get("speaker_a", "?")
    speaker_b = conv.get("speaker_b", "?")
    lines = [
        f"# Conversation {sample_id}",
        "",
        f"Participants: {speaker_a} and {speaker_b}",
        "",
    ]
    session_keys = sorted(
        (k for k in conv if k.startswith("session_") and "date" not in k),
        key=lambda k: int(k.split("_")[1]),
    )
    for sk in session_keys:
        date = conv.get(f"{sk}_date_time", "")
        turns = conv.get(sk) or []
        # Headers carry the timestamp inline so PageIndex's tree
        # summary-generator sees the date when it summarises a node.
        lines.append(f"## {sk.replace('_', ' ').title()} ({date})")
        lines.append("")
        for turn in turns:
            speaker = turn.get("speaker", "?")
            text = (turn.get("text") or "").replace("\n", " ").strip()
            dia_id = turn.get("dia_id", "")
            prefix = f"[{dia_id}] " if dia_id else ""
            lines.append(f"{prefix}**{speaker}**: {text}")
        lines.append("")
    return "\n".join(lines)


