async def _build_raw_tree(md_path: Path, model: str) -> dict:
    """Run md_to_tree once. Extracted so both file and store backends
    share the same expensive (~5-15 LLM calls) build step."""
    return await md_to_tree(
        str(md_path),
        if_add_node_summary="yes",
        summary_token_threshold=200,
        model=model,
        if_add_node_text="no",
        if_add_node_id="yes",
    )


def _conv_tree_dict(
    sample_id: str,
    md_text: str,
    tree: dict | list,
    session_dates: list[tuple[int, str]],
    *,
    document_id: str | None = None,
) -> dict:
    """Bundle the picker's working dict from the canonical pieces. The
    picker reads ``tree``/``md_text``/``session_dates``/``reference_date``;
    callers shouldn't construct this by hand.

    PR2 commit E: ``document_id`` is the store-assigned UUID for this
    sample's ``PageIndexDocument`` — used by the section recall path to
    address rows in section_entities / section_links queries. ``None``
    for the file backend.
    """
    return {
        "sample_id": sample_id,
        "md_text": md_text,
        "tree": tree,
        # Ordered list of (line_num, date_str) for the picker's temporal
        # routing prompt. Picker sees session anchors + their wall-clock
        # dates so it can binary-search to the right window.
        "session_dates": session_dates,
        # Anchor for the synthesiser's <reference_date> block — used to
        # resolve relative phrases like "two months ago" or "last week".
        # We pick the latest session as "now" because LoCoMo questions
        # are framed as if the user is looking back over the chat.
        "reference_date": session_dates[-1][1] if session_dates else None,
        # PR2-E: document_id for section recall lookup. None on file backend.
        "document_id": document_id,
    }


async def build_or_load_tree(
    conv: dict,
    sample_id: str,
    workspace: Path,
    model: str,
    *,
    store=None,  # PageIndexStore | None — when set, persist via SPI
    bank_id: str = "bench-pageindex-locomo",
    provider=None,  # PR2 commit A: LLMProvider for entity + embedding pass
    entity_model: str | None = None,
    embedding_model: str | None = None,
) -> dict:
    """Build a PageIndex tree for one conversation (or reuse cache).

    The tree-build is by far the most expensive one-time step
    (~5-15 LLM calls per conversation for summary generation). Caching
    by ``sample_id`` lets reruns of the bench skip it entirely while
    we iterate on the agent loop.

    Backends:
      - ``store=None`` (default): file-based cache at
        ``workspace/<sample_id>.tree.json`` (Phase A POC behaviour).
      - ``store=<PageIndexStore>``: PR1 commit C path — persist the
        tree as ``PageIndexDocument`` + ``PageIndexSection`` rows via
        the SPI. The picker still operates on the same in-memory dict
        shape; the only thing that changes is where the cached bytes
        live. Markdown is always re-written under ``workspace`` so the
        slicer can read it without round-tripping through the store
        (the store's md_text is the source of truth — the file mirror
        is just a fast-read scratch copy).
    """
    md_path = workspace / f"{sample_id}.md"

    # Always re-write the markdown — cheap and ensures the cached tree
    # was built from the current renderer's output. File-backend tree
    # cache is keyed off the markdown content via mtime check below;
    # store-backend uses (bank_id, source_id) + md_text equality.
    md_text = conversation_to_markdown(conv["conversation"], sample_id)
    md_path.write_text(md_text)

    if store is not None:
        return await _build_or_load_via_store(
            store=store,
            bank_id=bank_id,
            sample_id=sample_id,
            md_path=md_path,
            md_text=md_text,
            model=model,
            provider=provider,
            entity_model=entity_model,
            embedding_model=embedding_model,
        )

    # ── File-backend path (unchanged from Phase A) ──
    tree_path = workspace / f"{sample_id}.tree.json"
    if tree_path.exists() and tree_path.stat().st_mtime >= md_path.stat().st_mtime:
        cached = json.loads(tree_path.read_text())
    else:
        print(f"  [pageindex] Building tree for {sample_id} (file backend)...")
        cached = await _build_raw_tree(md_path, model)
        tree_path.write_text(json.dumps(cached, indent=2))

    nodes = cached.get("structure", cached) if isinstance(cached, dict) else cached
    session_dates = _enrich_nodes_with_dates(nodes)
    return _conv_tree_dict(sample_id, md_text, cached, session_dates)


async def _build_or_load_via_store(
    *,
    store,
    bank_id: str,
    sample_id: str,
    md_path: Path,
    md_text: str,
    model: str,
    provider=None,
    entity_model: str | None = None,
    embedding_model: str | None = None,
) -> dict:
    """PR1 commit D: tree-build path that persists through the
    ``PageIndexStore`` SPI. Cache key is ``(bank_id, sample_id)`` plus
    ``md_text`` equality (the conversation_to_markdown renderer is
    deterministic, so identical md_text means the cached sections are
    still valid)."""
    cached_doc = await store.load_document(bank_id, sample_id)
    cache_hit = cached_doc is not None and cached_doc.md_text == md_text

    if cache_hit:
        # Reconstruct the picker's nested-dict tree from the flat
        # sections. The picker prompt has been consuming this exact
        # shape since Phase A; the store-backed read path is shape-
        # equivalent to file-based load.
        sections = await store.load_skeleton(cached_doc.id)
        compact = _sections_to_compact_tree(sections)
        session_dates: list[tuple[int, str]] = [
            (s.line_num, _format_session_date(s.session_date))
            for s in sections
            if s.session_date is not None
        ]
        # Filter out any None date strings (shouldn't happen, but be safe).
        session_dates = [(ln, d) for ln, d in session_dates if d]
        return _conv_tree_dict(
            sample_id, md_text, compact, session_dates,
            document_id=cached_doc.id,
        )

    # Cache miss — run the expensive build, then persist.
    print(f"  [pageindex] Building tree for {sample_id} (store backend)...")
    raw_tree = await _build_raw_tree(md_path, model)
    nodes = raw_tree.get("structure", raw_tree) if isinstance(raw_tree, dict) else raw_tree
    session_dates = _enrich_nodes_with_dates(nodes)
    reference_date_dt = (
        _parse_session_date(session_dates[-1][1]) if session_dates else None
    )

    doc_to_save = PageIndexDocument(
        id="",  # ignored on insert; store assigns UUID and returns it
        bank_id=bank_id,
        source_id=sample_id,
        md_text=md_text,
        reference_date=reference_date_dt,
        built_at=datetime.now(tz=timezone.utc),
    )
    document_id = await store.save_document(doc_to_save)
    sections = _flatten_tree_to_sections(raw_tree, document_id)
    await store.save_sections(document_id, sections)

    # PR2 commit A: populate the entity + embedding indexes that PR2
    # commit B's parallel strategies will query against. Both calls are
    # gated on ``provider`` being available — when the bench is run
    # without a real LLM (smoke / file-backend mode), we skip cleanly so
    # the picker still works.
    if provider is not None:
        await _populate_section_index(
            provider=provider,
            store=store,
            document_id=document_id,
            sections=sections,
            md_text=md_text,
            entity_model=entity_model,
            embedding_model=embedding_model,
        )

    return _conv_tree_dict(
        sample_id, md_text, raw_tree, session_dates,
        document_id=document_id,
    )


async def _populate_section_index(
    *,
    provider,
    store,
    document_id: str,
    sections: list,
    md_text: str,
    entity_model: str | None,
    embedding_model: str | None,
) -> None:
    """PR2 commit A + D.7: extract entities + embed summaries +
    extract section links for every section in a freshly-built tree.

    Three passes, gathered concurrently:
    - Embeddings: one batched API call for the whole document.
    - Entities: one LLM call per section (~30 calls per LoCoMo conv).
    - Links (D.7): one LLM call per non-root section, looking back at
      the previous N section summaries for causal/supersedes/elaborates
      edges. The very first section has no prior, so it's skipped.

    Failures in any pass log a warning and degrade gracefully — the
    picker keeps working without these indexes, and PR2 commit B's
    strategies just return empty lists for unindexed sections.
    """
    from astrocyte.pipeline.section_link_extraction import (  # noqa: PLC0415
        extract_links_for_section,
    )

    # Sort sections by line_num so "prior" makes sense in document order.
    ordered_sections = sorted(sections, key=lambda s: s.line_num)

    # Run all three passes concurrently. Embeddings batch internally;
    # per-section LLM calls go in parallel via asyncio.gather.
    embed_task = embed_sections(provider, sections, model=embedding_model)
    entity_tasks = [
        extract_entities_for_section(
            provider, document_id, s,
            _slice_section_around_line(md_text, s.line_num),
            model=entity_model,
        )
        for s in sections
    ]
    # Link tasks: skip the first section (no prior context to link to).
    link_tasks = []
    for idx, s in enumerate(ordered_sections):
        if idx == 0:
            continue
        prior = ordered_sections[:idx]
        link_tasks.append(
            extract_links_for_section(
                provider, document_id, s,
                _slice_section_around_line(md_text, s.line_num),
                prior,
                model=entity_model,  # share the entity model knob; same gpt-4o-mini
            )
        )

    n_entity = len(entity_tasks)
    n_link = len(link_tasks)
    all_results = await asyncio.gather(
        embed_task, *entity_tasks, *link_tasks,
        return_exceptions=True,
    )
    embed_result = all_results[0]
    entity_results = all_results[1 : 1 + n_entity]
    link_results = all_results[1 + n_entity : 1 + n_entity + n_link]

    # Embeddings.
    n_embeddings = 0
    if isinstance(embed_result, Exception):
        print(f"  [pageindex] embed pass failed for doc={document_id}: {type(embed_result).__name__}")
    elif embed_result:
        await store.save_section_embeddings(document_id, embed_result)
        n_embeddings = len(embed_result)

    # Entities — flatten across sections, swallow per-section failures.
    all_entities = []
    for r in entity_results:
        if isinstance(r, Exception):
            continue
        all_entities.extend(r)
    if all_entities:
        await store.save_section_entities(all_entities)

    # Links (D.7) — flatten across sections, swallow per-section failures.
    all_links = []
    for r in link_results:
        if isinstance(r, Exception):
            continue
        all_links.extend(r)
    if all_links:
        await store.save_section_links(all_links)

    # PR2 D.7.1: semantic-kNN graph (no LLM cost). Runs AFTER
    # embeddings are saved so the SQL can read them. ``min_similarity``
    # filters near-zero noise — LME's chat-history shape produces lots
    # of weak similarities (different topics) that would only dilute
    # graph_expand if included.
    n_knn = 0
    if n_embeddings >= 2 and hasattr(store, "populate_semantic_knn_links"):
        try:
            n_knn = await store.populate_semantic_knn_links(
                document_id, top_k=5, min_similarity=0.5,
            )
        except Exception as exc:  # noqa: BLE001
            print(f"  [pageindex] semantic_knn failed for doc={document_id}: {type(exc).__name__}: {exc}")

    print(
        f"  [pageindex] indexed doc={document_id}: "
        f"{len(all_entities)} entities, "
        f"{n_embeddings} embeddings, "
        f"{len(all_links)} llm-links, "
        f"{n_knn} knn-links"
    )


# ── Date extraction (regex; free) ─────────────────────────────────────────
#
# Section headers carry a date that the picker / synth use for temporal
# routing. Two formats supported:
#
# - LoCoMo: "Session 1 (1:56 pm on 8 May, 2023)" → "8 May, 2023"
# - LME:    "Session 1 (2023/05/20 (Sat) 02:21)" → "20 May, 2023"
#           (PR2 commit A — without this, every LME tree has zero
#           session_dates and temporal mode is dead on LME).
#
# Both regexes return the same human-readable form ("D Month, YYYY") so
# downstream code (synth's ``<reference_date>`` block, picker's
# session_date_index) is shape-stable across benches.
_DATE_RE = re.compile(
    r"\(\s*(?:\d{1,2}:\d{2}\s*(?:am|pm)\s*on\s+)?"
    r"(\d{1,2}\s+[A-Za-z]+,?\s+\d{4})\s*\)",
    re.IGNORECASE,
)
# LME format: "(2023/05/20 (Sat) 02:21)" — ISO-style date inside parens.
# The weekday + time are noise; we only care about Y/M/D.
_LME_DATE_RE = re.compile(
    r"\(\s*(\d{4})/(\d{1,2})/(\d{1,2})"
    r"(?:\s*\([A-Za-z]{3}\))?(?:\s+\d{1,2}:\d{2})?\s*\)",
)
_MONTH_NAMES = [
    "", "January", "February", "March", "April", "May", "June",
    "July", "August", "September", "October", "November", "December",
]


def _extract_date_from_title(title: str) -> str | None:
    """Pull a normalised "D Month, YYYY" date out of a section title.

    Tries LoCoMo's `(1:56 pm on 8 May, 2023)` form first, then LME's
    `(2023/05/20 (Sat) 02:21)` ISO-with-weekday form. Returns ``None``
    when neither matches (caller falls back to NULL session_date)."""
    if not isinstance(title, str):
        return None
    m = _DATE_RE.search(title)
    if m:
        return m.group(1).strip()
    m = _LME_DATE_RE.search(title)
    if m:
        year, month, day = int(m.group(1)), int(m.group(2)), int(m.group(3))
        if 1 <= month <= 12:
            return f"{day} {_MONTH_NAMES[month]}, {year}"
    return None


def _enrich_nodes_with_dates(nodes: list | dict) -> list[tuple[int, str]]:
    """Walk a tree, attach `date` field on each node where parseable.

    Returns the ordered list of ``(line_num, date)`` pairs encountered —
    later passed to the picker so it sees a sortable date index.
    """
    found: list[tuple[int, str]] = []

    def _walk(n):
        if isinstance(n, list):
            for x in n:
                _walk(x)
            return
        if isinstance(n, dict):
            d = _extract_date_from_title(n.get("title", ""))
            if d:
                n["date"] = d
                ln = n.get("line_num")
                if isinstance(ln, int):
                    found.append((ln, d))
            sub = n.get("nodes")
            if sub:
                _walk(sub)

    _walk(nodes)
    return found


# ── Tree ↔ PageIndexSection conversion (PR1 commit D) ─────────────────────
#
# The picker operates on a nested-dict tree (post-``_strip_node_text``); the
# section recall store holds the same data as flat ``PageIndexSection`` rows. Two
# helpers bridge the shapes so we can persist trees to Postgres without
# touching the agent loop:
#
# - ``_flatten_tree_to_sections``: nested-tree → list[PageIndexSection]
#   (run after md_to_tree + ``_enrich_nodes_with_dates``; what gets written
#   to ``save_sections``).
# - ``_sections_to_compact_tree``: list[PageIndexSection] → nested dict
#   (run on cache-hit when reading from the store; produces the same shape
#   the picker prompt has been consuming since Phase A).

_MONTH_NUMBER = {
    name.lower(): idx
    for idx, name in enumerate(
        [
            "January", "February", "March", "April", "May", "June",
            "July", "August", "September", "October", "November", "December",
        ],
        start=1,
    )
}
_DATE_PARSE_RE = re.compile(r"(\d{1,2})\s+([A-Za-z]+),?\s+(\d{4})")


def _parse_session_date(raw: str | None) -> datetime | None:
    """Parse '8 May, 2023' → tz-aware UTC datetime. Returns None on failure
    so callers can fall back to ``session_date IS NULL`` semantics."""
    if not raw:
        return None
    m = _DATE_PARSE_RE.search(raw)
    if not m:
        return None
    day, month_name, year = m.groups()
    month = _MONTH_NUMBER.get(month_name.lower())
    if month is None:
        return None
    try:
        return datetime(int(year), month, int(day), tzinfo=timezone.utc)
    except ValueError:
        return None


def _format_session_date(dt: datetime | None) -> str | None:
    """Inverse of ``_parse_session_date``: '8 May, 2023' form. The picker
    prompt expects this human-readable shape."""
    if dt is None:
        return None
    return dt.strftime("%-d %B, %Y")


def _flatten_tree_to_sections(
    tree: dict | list, document_id: str,
) -> list[PageIndexSection]:
    """Walk a nested md_to_tree-shape (post-``_enrich_nodes_with_dates``)
    and return the flat ``PageIndexSection`` list to write to the store.

    Skips nodes without an integer ``line_num`` (the picker can't address
    them and the store's PK requires it).
    """
    nodes = tree.get("structure", tree) if isinstance(tree, dict) else tree
    out: list[PageIndexSection] = []

    def _walk(items, parent_node_id, depth):
        if not isinstance(items, list):
            return
        for n in items:
            if not isinstance(n, dict):
                continue
            line_num = n.get("line_num")
            if not isinstance(line_num, int):
                continue
            out.append(
                PageIndexSection(
                    document_id=document_id,
                    line_num=line_num,
                    node_id=n.get("node_id", "") or "",
                    title=n.get("title", "") or "",
                    summary=n.get("summary") or n.get("prefix_summary"),
                    speaker=None,  # PR2 commit C — speaker tag for LME assistant-recall
                    session_date=_parse_session_date(n.get("date")),
                    parent_node=parent_node_id,
                    depth=depth,
                )
            )
            sub = n.get("nodes")
            if sub:
                _walk(sub, n.get("node_id"), depth + 1)

    _walk(nodes, None, 0)
    return out


def _sections_to_compact_tree(
    sections: list[PageIndexSection],
) -> list[dict]:
    """Reconstruct the picker's nested-dict tree from a flat section list.

    Inverse of ``_flatten_tree_to_sections``. Groups by ``parent_node``
    (root nodes have ``parent_node=None``) and emits the same key shape
    the picker prompt has been consuming (title, node_id, line_num,
    summary, date, nested ``nodes``). The cache-hit path through the
    section recall store goes through this function.
    """
    by_parent: dict[str | None, list[PageIndexSection]] = {}
    for s in sections:
        by_parent.setdefault(s.parent_node, []).append(s)

    def _build(parent_node_id: str | None) -> list[dict]:
        children = sorted(
            by_parent.get(parent_node_id, []),
            key=lambda s: s.line_num,
        )
        out: list[dict] = []
        for s in children:
            node: dict = {
                "title": s.title,
                "node_id": s.node_id,
                "line_num": s.line_num,
            }
            if s.summary:
                node["summary"] = s.summary
            date_str = _format_session_date(s.session_date)
            if date_str:
                node["date"] = date_str
            sub = _build(s.node_id)
            if sub:
                node["nodes"] = sub
            out.append(node)
        return out

    return _build(None)


