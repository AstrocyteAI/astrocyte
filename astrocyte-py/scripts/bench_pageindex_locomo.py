async def _ask_picker(
    provider: OpenAIProvider, prompt: str, model: str,
    valid_lines: list[int],
) -> list[int]:
    """One picker call. Filters output to the allowlist, dedupes, caps
    at 12 (well above the picker's max-10 ask). When the picker
    returns an empty list (despite the prompt requiring ≥3 picks), or
    JSON parsing fails, falls back to a uniform sample across
    ``valid_lines`` so the synthesiser at least sees something — the
    synthesiser will then abstain correctly if the question truly has
    no answer in the conversation."""
    valid_set = set(valid_lines)
    pick_completion = await provider.complete(
        [Message(role="user", content=prompt)],
        model=model,
        max_tokens=300,
        temperature=0.0,
        response_format={"type": "json_object"},
    )
    out: list[int] = []
    try:
        pick = json.loads(pick_completion.text)
        raw = pick.get("line_nums") or []
        # Validate: only ints, only in allowlist, dedupe preserving order.
        seen: set[int] = set()
        for x in raw:
            try:
                n = int(x)
            except (TypeError, ValueError):
                continue
            if n in valid_set and n not in seen:
                seen.add(n)
                out.append(n)
            if len(out) >= 12:
                break
    except Exception:  # noqa: BLE001 — JSON parse fallback
        pass
    # Min-pick floor: smoke v5 (2026-05-09) showed the picker routinely
    # returning just [1] (title page) on multi-hop questions that asked
    # for 7-12 picks. That single pick slipped through the v4 safety
    # net (which only fired on empty/parse-fail), and the synthesiser
    # then hallucinated against the title-page excerpt.
    #
    # Padding strategy (revised v6→v7): use the FIRST N session anchors
    # (after stripping the title page line=1) rather than evenly-spaced.
    # Rationale: in LoCoMo, the relevant evidence overwhelmingly lives
    # in the early sessions where topics are introduced. Evenly-spaced
    # anchors over a 30-session conv hit lots of unrelated mid/late
    # sessions, which the synthesiser can't filter and which dilute
    # the right context. Early-bias matches the picker's natural
    # tendency to surface session 1 when uncertain.
    if not valid_lines:
        return out
    if len(out) < 4:
        # Drop the title-page line (line_num=1 is the document title,
        # not a session) when sampling. Keep it if it was a deliberate
        # pick, but don't pad with it.
        session_anchors = [ln for ln in valid_lines if ln > 1]
        seen2: set[int] = set(out)
        for ln in session_anchors:
            if ln not in seen2:
                out.append(ln)
                seen2.add(ln)
            if len(out) >= 5:
                break
    return out


