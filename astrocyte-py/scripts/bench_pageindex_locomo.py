def stratified_questions(
    locomo_data: list[dict], max_per_conversation: int,
) -> list[dict]:
    """Take up to N questions per conversation, balanced across
    categories. Mirrors ``bench-locomo-fair`` semantics so the result
    is comparable to the in-tree bench."""
    out: list[dict] = []
    for conv in locomo_data:
        sample_id = conv["sample_id"]
        qs = conv.get("qa") or []
        # Group by category and round-robin-pull until we hit the cap
        # or run out — matches the harness's stratified sampler.
        by_cat: dict[int, list[dict]] = {}
        for q in qs:
            by_cat.setdefault(q.get("category", 0), []).append(q)
        picks: list[dict] = []
        cat_iters = {c: iter(qlist) for c, qlist in by_cat.items()}
        while len(picks) < max_per_conversation and cat_iters:
            done: list[int] = []
            for c, it in list(cat_iters.items()):
                try:
                    picks.append(dict(next(it), conversation_id=sample_id))
                except StopIteration:
                    done.append(c)
                if len(picks) >= max_per_conversation:
                    break
            for c in done:
                cat_iters.pop(c, None)
        out.extend(picks)
    return out


