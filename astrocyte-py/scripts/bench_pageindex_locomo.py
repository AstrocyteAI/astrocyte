"""Phase A POC — vectorless reasoning-based RAG on LoCoMo via PageIndex.

Why this exists: the in-tree vector pipeline at HEAD scores ~50% on
LoCoMo on the same ``benchmarks/config-fast-recall.yaml`` that the
v0.13.0 baseline scored 83.28% with. The 33pp regression is in code
between v0.13.0 and HEAD and our config-knob bisect couldn't isolate
it. This script bypasses the entire vector retrieval path and tests
whether PageIndex's vectorless tree-search retrieval can land at or
above the v0.13.0 baseline with no Astrocyte pipeline involvement.

Pipeline:

1. **Markdown synthesis** — each LoCoMo conversation becomes one
   markdown document. Sessions are ``##`` headers including their
   wall-clock timestamp; turns are speaker-prefixed lines tagged with
   the original ``dia_id`` so the synthesised answer can cite the
   source. The headers are what PageIndex's tree-builder pivots on.

2. **Tree build (one-time per conversation)** — ``pageindex.md_to_tree``
   produces a hierarchical "table of contents" with per-node summary
   text generated by an LLM call. Built once before scoring; cached
   on disk so reruns skip rebuild.

3. **Per-question agent loop** — for each question:

   a. The conversation's tree skeleton (titles + summaries, no body
      text) is shown to an LLM along with the question.
   b. The LLM picks 1-3 ``line_num`` anchors that point at the
      sections most likely to contain the answer.
   c. We slice the markdown around those anchors (stop at the next
      ``##`` header) and feed the actual section text to a second LLM
      call which synthesises the final answer.

   That's a 2-call agent for the simple path. No tool-loop runtime
   because the search depth is bounded (one descend, one read) — the
   tree is small enough (a single conversation) that one round of
   navigation is sufficient. Aligns with PageIndex's "Vectorless RAG"
   recipe; we skip the OpenAI Agents SDK to keep the script
   dependency-free.

4. **Scoring** — reuses ``LoCoMoLLMJudge`` (the canonical yes/no judge
   the in-tree bench uses with ``--canonical-judge``) so results are
   directly comparable against v0.13.0's 83.28% baseline and the
   current pipeline's 50%.

Usage:
  doppler run -- uv run python scripts/bench_pageindex_locomo.py \\
      --max-questions-per-conversation 20

Targets:
  - overall ≥ 75%   (vs vector pipeline at HEAD: 50%)
  - adversarial ≥ 90%  (vs vector pipeline: 24-71%)
  - if either is reached, port to Astrocyte as a recall_strategy.
"""

