def _extract_question_nouns(question: str) -> list[str]:
    """PR2 D.2: pull lowercase content nouns from the question.

    Catches the common-noun anchors that ``_extract_question_entities``
    misses — words like 'pendant', 'obesity', 'dog treats', 'taekwondo',
    'France' that the open-domain failure analysis identified as the
    pivot for specific-fact questions.

    Heuristic: tokens of length ≥4 that aren't in the stoplist. We
    over-include rather than under-include — the noun-rescue grep
    filters by document content, so false-positive nouns just don't
    match anything.
    """
    # Lowercase + strip punctuation, split on whitespace
    cleaned = re.sub(r"[^\w\s]", " ", question.lower())
    tokens = cleaned.split()
    out: list[str] = []
    seen: set[str] = set()
    for t in tokens:
        if len(t) < 4:
            continue
        if t in _NOUN_RESCUE_STOPLIST:
            continue
        if t.isdigit():
            continue
        if t in seen:
            continue
        seen.add(t)
        out.append(t)
    return out


def _rescue_specific_fact_lines(
    question_nouns: list[str],
    md_text: str,
    *,
    valid_lines: list[int],
    max_rescues: int = 3,
) -> list[int]:
    """PR2 D.2: regex-grep the conversation markdown for question nouns.
    Return the line_nums of sessions whose body contains any noun.

    The picker / rerank pipeline routes on tree summaries; specific-fact
    questions ('country Jolene's mother bought pendant') hinge on body
    nouns that summaries elide. This function bypasses the summary
    layer and goes straight to the markdown.

    ``max_rescues`` caps the lift — we want to *augment* the picker's
    selection, not replace it. Returns a sorted list of line_nums to
    union into the picker's final picks.
    """
    if not question_nouns or not md_text or not valid_lines:
        return []
    # Build a regex matching any of the nouns as a word boundary.
    # Sort by length desc so longer nouns get checked first (more specific).
    sorted_nouns = sorted(set(question_nouns), key=len, reverse=True)
    pattern = re.compile(
        r"\b(" + "|".join(re.escape(n) for n in sorted_nouns) + r")\b",
        re.IGNORECASE,
    )

    lines = md_text.split("\n")
    valid_set = sorted(set(valid_lines))
    if not valid_set:
        return []

    # For each valid_line (a section anchor), check the section body
    # (from this line to the next valid_line) for a noun match. Return
    # at most ``max_rescues`` line_nums, ranked by hit-count desc so
    # the most-relevant section comes first.
    section_hits: list[tuple[int, int]] = []  # (line_num, hit_count)
    for i, ln in enumerate(valid_set):
        start = ln - 1  # 1-indexed line_num
        end = (valid_set[i + 1] - 1) if i + 1 < len(valid_set) else len(lines)
        if start < 0 or start >= len(lines):
            continue
        body = "\n".join(lines[start:end])
        hits = len(pattern.findall(body))
        if hits > 0:
            section_hits.append((ln, hits))

    section_hits.sort(key=lambda x: x[1], reverse=True)
    return [ln for ln, _ in section_hits[:max_rescues]]


def _extract_question_entities(question: str) -> list[str]:
    """Heuristic proper-noun extraction from a question (PR2 commit E).

    Pulls every Capitalised word, drops question-stopwords (``What``,
    ``When``, ``Did``, etc.). Replaced by an LLM-based extractor in
    PR2 commit D when the heuristic mis-routes too often.

    Each token is emitted separately — multi-word names like ``John
    Smith`` yield ``['John', 'Smith']`` so the entity strategy's
    case-insensitive ANY match in section_entities still hits. Dedupe
    is post-step.
    """
    tokens = re.findall(r"\b[A-Z][a-zA-Z]+\b", question)
    return list({t for t in tokens if t not in _QUESTION_STOPWORDS})


async def answer_question(
    provider: OpenAIProvider,
    conv_tree: dict,
    question: str,
    model: str,
    *,
    store=None,  # PageIndexStore | None — when set, use section recall path (PR2-E)
    bank_id: str | None = None,
    cross_encoder=None,  # CrossEncoderProtocol | None
    category: str | None = None,  # PR2-D.1: dataset-provided category label
) -> tuple[str, list[int]]:
    """Run the 2-step PageIndex agent loop for one question, with an
    iterative-expansion fallback when the first pick yields "Not in
    memories" on a non-adversarial-shaped question.

    PR2 commit E: when ``store`` + ``bank_id`` are provided AND the
    conv_tree carries a ``document_id``, the picker operates on a
    constrained skeleton produced by ``section_recall`` → cross-encoder
    rerank (top-15). Without those, falls back to the PR1 picker-on-
    full-skeleton path.

    Returns ``(answer, picked_line_nums)``.
    """
    # Step 1: pick.
    # ``md_to_tree`` returns ``{doc_name, line_count, structure: [...]}``.
    # The picker only cares about the navigable node list, so unwrap
    # to ``structure`` when present (older shapes returned a bare list).
    raw_tree = conv_tree["tree"]
    nodes = raw_tree.get("structure", raw_tree) if isinstance(raw_tree, dict) else raw_tree
    skeleton = _strip_node_text(nodes)
    valid_lines = _collect_valid_line_nums(skeleton)
    tree_json = json.dumps(skeleton, indent=2)

    # Mode dispatch — pick prompt and synth prompt match the question
    # shape. Listing/temporal questions need different selection biases
    # AND different synth modes (enumerate vs single-fact compose).
    # PR2-D.1: prefer the dataset's category label when provided —
    # the regex heuristic mis-routes LME's category names entirely.
    mode = _detect_question_mode(question, category=category)

    # PR2-E: section recall pre-filter. Run when the store backend has
    # populated entity + embedding indexes (PR2-A) so the orchestrator
    # has real signal to fuse. The picker still operates on a nested-
    # dict skeleton; we just narrow it to the top-15 reranked sections.
    document_id = conv_tree.get("document_id")
    # PR2 D.5.5: ``sections_by_key`` is hoisted to the outer scope so
    # the temporal-arithmetic short-circuit (later in this function)
    # can reach it. Built inside the section recall block; defaults to {}.
    sections_by_key: dict[tuple[str, int], PageIndexSection] = {}
    if store is not None and bank_id and document_id:
        from astrocyte.pipeline.question_annotator import annotate_question
        from astrocyte.pipeline.section_recall import section_recall  # local import to avoid heavy load on file backend
        from astrocyte.pipeline.section_rerank import (
            build_constrained_skeleton,
            rerank_fused_hits,
        )

        # PR2 D.5: single LLM call returns entities + date_range.
        # Replaces the regex ``_extract_question_entities`` (which only
        # caught Capitalised proper nouns) and the manual conv-range
        # date_range computation (which spanned the WHOLE conversation
        # and gave the temporal strategy nothing to filter on — that's
        # why LME temporal-reasoning stayed at 0% in PR2-D.1-4).
        annotation = await annotate_question(
            provider, question,
            reference_date=conv_tree.get("reference_date"),
            model=model,
        )
        question_entities = annotation.entities
        date_range = annotation.date_range

        # PR2 D.5-fallback: when the question is temporally-routed but
        # the LLM didn't extract a specific date anchor ("When did X
        # happen?" — no date in the question text), fall back to the
        # conversation's full date range so the temporal strategy at
        # least contributes session-date ordering. Without this fallback
        # LoCoMo temporal regresses 55→45 because temporal strategy
        # silently doesn't fire on date-less temporal questions.
        if (
            date_range is None
            and mode in {"temporal", "temporal-reasoning"}
            and conv_tree.get("session_dates")
        ):
            from datetime import timedelta
            parsed = [
                _parse_session_date(d) for _, d in conv_tree["session_dates"]
            ]
            parsed = [p for p in parsed if p is not None]
            if parsed:
                date_range = (min(parsed), max(parsed) + timedelta(days=1))

        try:
            recall_result = await section_recall(
                store=store, bank_id=bank_id, question=question, mode=mode,
                embedding_provider=provider,
                question_entities=question_entities or None,
                date_range=date_range,
            )
        except Exception as exc:  # noqa: BLE001
            print(f"  [pageindex] section_recall failed for q={question[:40]!r}: {type(exc).__name__}: {exc}")
            recall_result = None

        if recall_result and recall_result.fused:
            # Build sections_by_key from the existing skeleton — no
            # extra DB call. (Hoisted to outer scope above; just
            # populate here.)

            def _walk_collect(items):
                if not isinstance(items, list):
                    return
                for n in items:
                    if not isinstance(n, dict):
                        continue
                    ln = n.get("line_num")
                    if isinstance(ln, int):
                        # PR2 D.5.5c: populate session_date from the
                        # in-memory tree's ``date`` string (set by
                        # ``_enrich_nodes_with_dates`` at build time).
                        # Without this, every section had session_date=None
                        # and D.5.5 had to lazy-load from the store.
                        sections_by_key[(document_id, ln)] = PageIndexSection(
                            document_id=document_id,
                            line_num=ln,
                            node_id=n.get("node_id", ""),
                            title=n.get("title", "") or "",
                            summary=n.get("summary"),
                            session_date=_parse_session_date(n.get("date")),
                            depth=0,
                        )
                    _walk_collect(n.get("nodes"))

            _walk_collect(skeleton)
            try:
                # PR2-C tuning: cut at top-25 instead of top-15 — first
                # smoke (30% overall vs PR1's 62%) showed multi-hop
                # questions losing the second-source session when the
                # cross-encoder ranked it below 15. Multi-hop needs
                # ≥2 sources; the rerank is too confident on single-
                # best matches. Top-25 drops only the bottom 5 of a
                # ~30-section conv — keeps almost everything visible
                # while still removing clearly-irrelevant nodes.
                #
                # We ALSO keep the original ``valid_lines`` (full set)
                # as the picker's allowlist — the constrained skeleton
                # is a strong hint, but the picker can still pick any
                # line_num if it disagrees with the rerank. This is
                # the v6 lesson re-applied: don't lock the picker out
                # of evidence the orchestrator missed.
                reranked = rerank_fused_hits(
                    recall_result.fused, sections_by_key, question,
                    model=cross_encoder, rerank_top_k=30, output_top_k=25,
                )
                if reranked:
                    keep_keys = {(h.document_id, h.line_num) for h in reranked}
                    constrained = build_constrained_skeleton(
                        skeleton, keep_keys, document_id,
                    )
                    if constrained:
                        # NOTE: ``skeleton`` and ``valid_lines`` deliberately
                        # NOT narrowed — the picker can still address sections
                        # outside the constrained tree if it remembers the
                        # topic. Only the JSON tree shown to the LLM is
                        # rebuilt to highlight the reranked subset.
                        tree_json = json.dumps(constrained, indent=2)
            except Exception as exc:  # noqa: BLE001
                print(f"  [pageindex] rerank failed: {type(exc).__name__}: {exc}")
    reference_date = conv_tree.get("reference_date") or "unknown"
    session_dates = conv_tree.get("session_dates") or []
    session_date_index = "\n".join(
        f"  line {ln}: {d}" for ln, d in session_dates
    ) or "  (none — header dates not parseable)"

    # PR2.5: counting/sum dispatch — REVERTED.
    # Tested: counting synth correctly produces a number ("2 items",
    # "$65") but PICKER still undercounts (fetches 5-6 sections when
    # answer requires 8-10 mentions). Multi-session unchanged at 1/9;
    # other categories regressed (single-session-user 78→67, single-
    # session-preference 38→25, LME overall 46→42).
    # Counting needs DETERMINISTIC entity-based aggregation over all
    # mentioning sections (not LLM), which is a separate piece of
    # work. Accepting LME multi-session at 11% for PR2 close.
    is_counting = False

    if mode == "temporal":
        pick_msg = PICK_PROMPT_TEMPORAL.format(
            tree_json=tree_json,
            question=question,
            valid_lines=valid_lines,
            session_date_index=session_date_index,
            reference_date=reference_date,
        )
    elif mode == "listing" or is_counting:
        pick_msg = PICK_PROMPT_LISTING.format(
            tree_json=tree_json,
            question=question,
            valid_lines=valid_lines,
        )
    else:
        pick_msg = PICK_PROMPT_DEFAULT.format(
            tree_json=tree_json,
            question=question,
            valid_lines=valid_lines,
        )
    line_nums = await _ask_picker(provider, pick_msg, model, valid_lines)

    # PR2 D.2: question-noun rescue. Specific-fact questions ('country
    # Jolene's mother bought pendant') hinge on body nouns that
    # summaries elide — the picker can't see them. Grep the markdown
    # for question nouns and union the matching sessions into the
    # picker's selection. We DON'T replace the picker — just augment.
    # No-op when the question has no extractable nouns or no body
    # match (so listing/inference questions aren't perturbed).
    #
    # Disabled for:
    # - adversarial: LoCoMo adversarial works best when synth abstains;
    #   injecting noun-rescued sections gives it false context.
    # - temporal modes: subject names (Caroline, John) appear in nearly
    #   every session; noun-rescue floods the synth with off-date
    #   sessions and dilutes the temporal signal. The temporal strategy
    #   already pre-filters by date — let it do its job. PR2-D.1-4
    #   gate showed -10pp on LoCoMo temporal when rescue was active
    #   for these modes.
    if category != "adversarial" and mode not in {"temporal", "temporal-reasoning"}:
        question_nouns = _extract_question_nouns(question)
        rescued = _rescue_specific_fact_lines(
            question_nouns, conv_tree["md_text"],
            valid_lines=valid_lines, max_rescues=3,
        )
        if rescued:
            # Union with picker output, preserving picker's order first
            # (the picker's reasoning is primary; rescue is augmentation).
            seen_ln = set(line_nums)
            for ln in rescued:
                if ln not in seen_ln:
                    line_nums.append(ln)
                    seen_ln.add(ln)

    # Step 2: fetch sections.
    if line_nums:
        excerpts = "\n\n---\n\n".join(
            _slice_section_around_line(conv_tree["md_text"], ln)
            for ln in line_nums
        ).strip()
    else:
        excerpts = ""

    if not excerpts:
        # The picker couldn't (or wouldn't) route. Tell the synthesizer
        # nothing's in scope — let it answer "Not in the provided
        # memories." which is the right outcome on adversarial Qs.
        excerpts = "(no relevant section found)"

    # PR2 D.5.5c: structured-dates synth context — REVERTED.
    # Tested in two gates:
    #   v1 (both temporal modes): LoCoMo temporal 55→27% (synth used
    #     session_date as event_date), LME temporal-reasoning still 0%.
    #   v2 (LME-only gate): LME temporal-reasoning still 0% AND
    #     other LME categories regressed (multi-session 11→0,
    #     single-session-* down 10pp each).
    # Both runs proved: the structured-dates block either substitutes
    # session_date for the correct in-dialogue event date (LoCoMo) or
    # doesn't help arithmetic the synth can't do anyway (LME).
    #
    # The session_date population in _walk_collect (above) STAYS as a
    # bug fix — it lets D.5.5's arithmetic short-circuit work without
    # lazy-loading from the store.

    # Listing questions get the union/dedupe synth; everything else
    # uses the default synth (which now carries the reference_date
    # block for temporal-relative phrases).
    #
    # PR2 D.3: inference questions get a permissive synth that's
    # instructed to read between the lines. Routes when:
    #   - Category is open-domain or single-session-preference (dataset signal), OR
    #   - The question text matches an inference pattern (regex fallback).
    # PR2 D.4: answer-type hint goes to the inference synth so it
    # verifies its final answer matches the asked-for type.
    is_inference = (
        category in {"open-domain", "single-session-preference"}
        or _is_inference_question(question)
    )

    # PR2 D.5.5: programmatic date-arithmetic short-circuit. LME
    # temporal-reasoning is 0% across PR2/D.1-4/D.4-fix/D.5 because
    # questions like "how many days between A and B?" require date
    # arithmetic gpt-4o-mini can't reliably do. We have all three
    # dates structured in section.session_date — compute in Python.
    #
    # Only fires when:
    #   - The store-backed section recall path was taken (sections_by_key was built).
    #   - mode is temporal/temporal-reasoning (gates against false-positive
    #     regex matches in non-temporal questions).
    #   - The arithmetic module recognizes the question shape AND finds
    #     the events in the document. Otherwise falls through to synth.
    arithmetic_answer: str | None = None
    if (
        store is not None and bank_id and document_id
        and mode in {"temporal", "temporal-reasoning"}
    ):
        from astrocyte.pipeline.temporal_arithmetic import (
            compute_temporal_arithmetic_answer,
        )
        # ``sections_by_key`` was built earlier in the section recall block;
        # reach for it from the outer scope. ``reference_date_dt`` is
        # parsed from conv_tree.reference_date (a string).
        ref_dt = _parse_session_date(reference_date) if reference_date != "unknown" else None
        try:
            arithmetic_answer = await compute_temporal_arithmetic_answer(
                store=store,
                bank_id=bank_id,
                document_id=document_id,
                question=question,
                sections_by_key=sections_by_key,
                reference_date_dt=ref_dt,
            )
        except Exception as exc:  # noqa: BLE001
            print(f"  [pageindex] temporal_arithmetic failed: {type(exc).__name__}: {exc}")
            arithmetic_answer = None

    if arithmetic_answer is not None:
        # Skip the synth — return the computed answer directly.
        answer = arithmetic_answer
        return answer, line_nums

    if is_counting:
        # PR2.5: counting questions use a dedicated synth that
        # outputs a NUMBER (not an enumeration). Picker stayed as
        # listing-shape so synth has many sections to count over.
        syn_msg = SYNTHESIZE_PROMPT_COUNTING.format(
            excerpts=excerpts, question=question, reference_date=reference_date,
        )
        syn_max_tokens = 80  # answers are short ("3", "$185") — cap tight to suppress enumeration
    elif mode == "listing":
        syn_msg = SYNTHESIZE_PROMPT_LISTING.format(
            excerpts=excerpts, question=question, reference_date=reference_date,
        )
        syn_max_tokens = 400  # listing answers are longer than 1-fact answers
    elif is_inference and category != "adversarial":
        syn_msg = SYNTHESIZE_PROMPT_INFERENCE.format(
            excerpts=excerpts, question=question, reference_date=reference_date,
            answer_type_hint=_detect_answer_type_hint(question) or "(no hint)",
        )
        syn_max_tokens = 200
    else:
        syn_msg = SYNTHESIZE_PROMPT_DEFAULT.format(
            excerpts=excerpts, question=question, reference_date=reference_date,
        )
        syn_max_tokens = 250

    syn_completion = await provider.complete(
        [Message(role="user", content=syn_msg)],
        model=model,
        max_tokens=syn_max_tokens,
        temperature=0.0,
    )
    answer = syn_completion.text.strip()

    # Iterative expansion: if the first attempt returned the
    # abstention sentinel AND the picker DID select sections (i.e.
    # the picker thought there was something there but the picked
    # sections didn't pan out), retry with a broader pick. This
    # specifically targets multi-hop/temporal questions where the
    # first picker undershoots — the smoke run on 2026-05-09
    # observed the picker routinely selecting 1-2 sections and
    # missing 3-4 other sessions that contained chained evidence.
    #
    # We DON'T retry when the picker returned an empty list — that's
    # the adversarial-correct outcome (no relevant section exists)
    # and re-running would just risk fabricating a wrong answer.
    abstained = "not in the provided memories" in answer.lower()
    if abstained and line_nums and len(line_nums) < len(valid_lines):
        expand_msg = EXPAND_PROMPT.format(
            tree_json=tree_json,
            question=question,
            valid_lines=valid_lines,
            prior_lines=line_nums,
        )
        wider_lines = await _ask_picker(provider, expand_msg, model, valid_lines)
        # Merge with prior picks — neighbours might still be useful.
        merged = list(dict.fromkeys([*line_nums, *wider_lines]))[:12]
        if merged != line_nums:  # actually expanded
            wider_excerpts = "\n\n---\n\n".join(
                _slice_section_around_line(conv_tree["md_text"], ln)
                for ln in merged
            ).strip() or "(no relevant section found)"
            # Use the same mode-appropriate synth on expansion. Listing
            # mode benefits most from the wider sweep (more items to
            # enumerate); temporal/default benefit from the broader
            # multi-hop chain assembly.
            if mode == "listing":
                syn_msg2 = SYNTHESIZE_PROMPT_LISTING.format(
                    excerpts=wider_excerpts, question=question,
                    reference_date=reference_date,
                )
                syn2_max = 400
            else:
                syn_msg2 = SYNTHESIZE_PROMPT_DEFAULT.format(
                    excerpts=wider_excerpts, question=question,
                    reference_date=reference_date,
                )
                syn2_max = 300
            syn_completion2 = await provider.complete(
                [Message(role="user", content=syn_msg2)],
                model=model,
                max_tokens=syn2_max,
                temperature=0.0,
            )
            answer = syn_completion2.text.strip()
            line_nums = merged
    return answer, line_nums


