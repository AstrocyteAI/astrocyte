def _strip_node_text(tree: list | dict) -> list | dict:
    """Strip ``text`` field from tree nodes, preserving structure +
    summaries. PageIndex sometimes carries body text on nodes; the
    skeleton we pass to the picker LLM should NOT include it (the
    whole point of the two-step retrieval is the LLM picks before
    seeing the bodies)."""
    if isinstance(tree, list):
        return [_strip_node_text(n) for n in tree]
    if isinstance(tree, dict):
        out = {k: v for k, v in tree.items() if k != "text"}
        if "nodes" in out:
            out["nodes"] = _strip_node_text(out["nodes"])
        return out
    return tree


def _slice_section_around_line(md_text: str, line_num: int) -> str:
    """Return the markdown section starting at ``line_num`` until the
    next ``##`` header (or EOF). 1-indexed line numbers (matches
    PageIndex's ``line_num`` field convention)."""
    lines = md_text.split("\n")
    if line_num < 1 or line_num > len(lines):
        return ""
    out_lines: list[str] = [lines[line_num - 1]]
    for line in lines[line_num:]:
        if line.startswith("## "):
            break
        out_lines.append(line)
    return "\n".join(out_lines).strip()


def _collect_valid_line_nums(tree: list | dict, out: list[int] | None = None) -> list[int]:
    """Extract every section-anchor ``line_num`` from a tree.

    The picker's job is to choose ``##`` section anchors — the
    smoke run on 2026-05-09 showed gpt-4o-mini routinely hallucinated
    line numbers that fell inside a section's body (or in the
    pre-Session-1 conversation header), and the slice helper then
    returned garbage that synthesised to "Not in the provided
    memories". Restricting picks to this allowlist eliminates that
    failure class entirely. Sorted ascending so the picker prompt
    can present them as a consistent menu.
    """
    if out is None:
        out = []
    if isinstance(tree, list):
        for n in tree:
            _collect_valid_line_nums(n, out)
        return out
    if isinstance(tree, dict):
        ln = tree.get("line_num")
        if isinstance(ln, int):
            out.append(ln)
        if tree.get("nodes"):
            _collect_valid_line_nums(tree["nodes"], out)
    return sorted(set(out))


# ── Question-mode detection ────────────────────────────────────────────
#
# LoCoMo categories drive different retrieval shapes:
#
# - temporal ("when did X?", "how long after Y?", "in May 2023") — needs
#   date-anchored picking + a reference_date the synthesiser can use to
#   resolve relative phrases.
# - open-domain / listing ("everything about X", "all the jobs Jon held",
#   "which X do they like") — needs *every* session mentioning the
#   subject, not the top-k. Synthesiser must enumerate + dedupe instead
#   of producing a one-sentence answer.
# - multi-hop (default for compound questions) — needs a bigger pick
#   budget so chained evidence can land in scope.
#
# Heuristic regex over the question text. Cheap; mis-routing back to
# default is harmless (default is the strict superset of multi-hop).
_LISTING_PATTERNS = [
    r"\beverything\b",
    r"\ball\s+(?:the\s+|of\s+)?",
    r"\bevery\s+",
    r"\blist\s+(?:the\s+|all\s+)?",
    r"\bwhat\s+are\s+(?:the|all)\b",
    r"\bnames?\s+of\s+",
    r"\bwhich\s+\w+s\b",  # plural noun → likely listing
]

# PR2 D.7.2: counting/sum question patterns. LME multi-session is
# 78% counting/sum questions ("how many doctors", "how much spent",
# "total hours"). The PR2-D.7 gate showed graph_expand correctly
# bridges sessions but the synth undercounts because the picker
# returns top-K (5-6) sections — insufficient for exhaustive
# enumeration when the answer needs 8-10 mentions.
#
# Routing these to ``listing`` mode (PICK_PROMPT_LISTING +
# SYNTHESIZE_PROMPT_LISTING) makes the picker fetch ALL relevant
# sections, which is the same fix that lifted LoCoMo open-domain
# from 33→67% in PR2-D.1-4 for "every / all / list" questions.
_COUNTING_PATTERNS = [
    r"\bhow\s+many\s+(?!days?\s+(?:passed|elapsed|ago)|weeks?\s+(?:passed|elapsed|ago)|months?\s+(?:passed|elapsed|ago)|years?\s+(?:passed|elapsed|ago))",
    r"\bhow\s+much\s+(?:total|money|did|do|have)",
    r"\b(?:total|sum)\s+(?:number|amount|money|cost|hours?|days?|weeks?|months?|years?)\b",
    r"\bcount\s+(?:of|the)\s+",
]


def _is_counting_question(question: str) -> bool:
    """PR2 D.7.2: detect counting/sum questions that need listing-shape
    retrieval. The ``how many days passed`` / ``how many weeks ago``
    patterns are EXCLUDED — those are handled by D.5.5's date-arithmetic
    short-circuit, not listing.
    """
    q = question.lower()
    return any(re.search(p, q) for p in _COUNTING_PATTERNS)

_TEMPORAL_PATTERNS = [
    r"\bwhen\s+(?:did|was|were|does|will|do)\b",
    r"\bwhat\s+(?:date|day|month|year|time)\b",
    r"\bhow\s+(?:long|many\s+(?:days|weeks|months|years))\b",
    r"\bafter\s+how\s+",
    r"\bbefore\s+(?:she|he|they|going|the)\b",
    r"\b(?:january|february|march|april|may|june|july|august|september|october|november|december)\b",
    r"\b(?:19|20)\d{2}\b",  # year mention
    r"\blast\s+(?:week|month|year|night|time)\b",
    r"\b(?:two|three|four|five|six|seven|eight|nine|ten|\d+)\s+(?:days?|weeks?|months?|years?)\s+(?:ago|later|before|after)\b",
]


# PR2 D.3: regex patterns for "this is an inference question" — used
# both for LoCoMo open-domain (most of which are inference-shaped, not
# listing-shaped) and LME single-session-preference. When detected,
# the synth uses ``SYNTHESIZE_PROMPT_INFERENCE`` which is permissive
# about reading-between-the-lines instead of abstaining.
_INFERENCE_PATTERNS = [
    r"\bwould\s+\w+\s+(enjoy|like|prefer|want|consider|choose|appreciate)",
    r"\bwhat\s+(might|may)\b",
    r"\bis\s+it\s+(likely|possible|probable)\b",
    r"\bdo\s+you\s+think\b",
    r"\bcould\s+\w+\s+(have|be|do)",
    r"\bwhat['']s\s+an?\s+\w+\s+\w+\s+would\s+(like|enjoy)",  # "what's an activity X would enjoy"
    r"\bgiven\s+(?:that|what)\b",  # "given what we know about X"
]


# PR2 D.4: answer-type hint table. Maps question-shape patterns to
# the type the answer MUST match. The inference synth uses this to
# verify its final answer before emitting (avoids the "Boston" =
# city-not-country failure).
_ANSWER_TYPE_PATTERNS = [
    (r"\bwhat\s+country\b|\bwhich\s+country\b|\bin\s+what\s+country\b", "country"),
    (r"\bwhat\s+city\b|\bwhich\s+city\b|\bin\s+what\s+city\b", "city"),
    (r"\bwhat\s+year\b|\bwhich\s+year\b|\bin\s+what\s+year\b", "year"),
    (r"\bwhat\s+month\b|\bwhich\s+month\b", "month"),
    (r"\bwhat\s+(?:date|day)\b", "date"),
    (r"\bwho\b.*\b(?:did|was|met|saw|met with)\b", "person"),
    (r"\bhow\s+old\b|\bwhat\s+age\b", "age (number)"),
    (r"\bhow\s+(many|much)\b", "number / quantity"),
]


def _detect_answer_type_hint(question: str) -> str:
    """PR2 D.4: emit a one-word hint about the expected answer type.
    The inference synth uses this to verify its final answer matches
    the asked-for type. Returns an empty string when no hint applies."""
    q = question.lower()
    for pattern, hint in _ANSWER_TYPE_PATTERNS:
        if re.search(pattern, q):
            return hint
    return ""


def _is_inference_question(question: str) -> bool:
    """PR2 D.3: classify whether a question is inference-shape."""
    q = question.lower()
    return any(re.search(p, q) for p in _INFERENCE_PATTERNS)


# PR2 D.1: explicit category → mode mapping for benches that already
# carry a question_type label. LoCoMo uses these category integers
# (mapped via ``_CATEGORY_MAP``); LME uses string labels directly.
# Both should bypass the regex heuristic — the dataset *knows* which
# kind of question this is.
_CATEGORY_TO_MODE = {
    # LoCoMo (string labels post-_CATEGORY_MAP)
    "multi-hop": "multi-hop",
    "temporal": "temporal",
    "open-domain": "default",  # most open-domain are inference; D.3 adds inference mode
    "single-hop": "default",
    "adversarial": "default",
    # LME (string labels from question_type)
    "single-session-user": "default",
    "single-session-assistant": "single-session-assistant",
    "single-session-preference": "default",  # inference-shape; D.3 will route to 'inference'
    "multi-session": "multi-session",
    "temporal-reasoning": "temporal-reasoning",
    "knowledge-update": "knowledge-update",
}


def _detect_question_mode(
    question: str,
    *,
    category: str | None = None,
) -> str:
    """Return a mode label for picker / synth dispatch.

    PR2 D.1: when ``category`` is provided (every bench question has
    one), use the explicit category → mode map. The regex heuristic is
    only a fallback for callers without a category — neither LoCoMo
    nor LME hits that path. The previous implementation ignored
    category and returned wrong modes for every LME multi-session /
    temporal-reasoning question, which is why those categories scored
    0% in the PR2 LME gate.

    Returned modes are passed verbatim to ``section_recall``'s
    ``select_strategies_for_mode``; both ends agree on the same string
    constants.
    """
    if category:
        mapped = _CATEGORY_TO_MODE.get(category)
        if mapped:
            return mapped

    # Fallback: regex heuristic on question text. Used for ad-hoc
    # callers without a category label.
    q = question.lower()
    for p in _LISTING_PATTERNS:
        if re.search(p, q):
            return "listing"
    for p in _TEMPORAL_PATTERNS:
        if re.search(p, q):
            return "temporal"
    return "default"


